var $star_rating46 = $('.star-rating46 .fa');
var $form46 = $('#starForm46');

var SetRatingStar = function() {
    return $star_rating46.each(function() {
        if (parseInt($star_rating46.siblings('input.rating-value').val()) >= parseInt($(this).data('rating'))) {
            return $(this).removeClass('fa-star-o').addClass('fa-star');
        } else {
            return $(this).removeClass('fa-star').addClass('fa-star-o');
        }
    });
};



SetRatingStar();

$star_rating46.on('click', async function() {

    $star_rating46.siblings('input.rating-value').val($(this).data('rating'));
    await SetRatingStar();
    $form46.submit();
});

